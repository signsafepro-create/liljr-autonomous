import os, sys, json, re, time, traceback, hashlib, inspect

class AutoCoder:
    def __init__(self, base_path):
        self.base = os.path.expanduser(base_path)
        os.makedirs(self.base, exist_ok=True)
        self.logs = []
    
    def log(self, msg):
        entry = f"[{time.strftime('%H:%M:%S')}] {msg}"
        self.logs.append(entry)
        print(f"[AUTOCODER] {entry}")
    
    # ─── FILE OPERATIONS ───
    def read_file(self, path):
        full = os.path.join(self.base, path)
        try:
            with open(full, 'r') as f:
                return f.read()
        except:
            return None
    
    def write_file(self, path, content):
        full = os.path.join(self.base, path)
        os.makedirs(os.path.dirname(full) or '.', exist_ok=True)
        with open(full, 'w') as f:
            f.write(content)
        self.log(f"Wrote {len(content)} chars to {path}")
        return {"status": "written", "path": path, "size": len(content)}
    
    def list_files(self, pattern='.*'):
        files = []
        for root, _, filenames in os.walk(self.base):
            for f in filenames:
                rel = os.path.relpath(os.path.join(root, f), self.base)
                if re.match(pattern, rel):
                    files.append(rel)
        return files
    
    # ─── CODE ANALYSIS ───
    def analyze_file(self, path):
        content = self.read_file(path)
        if not content:
            return {"error": "File not found"}
        
        analysis = {
            "path": path,
            "size": len(content),
            "lines": content.count('\n') + 1,
            "functions": re.findall(r'def\s+(\w+)\s*\(', content),
            "classes": re.findall(r'class\s+(\w+)', content),
            "imports": re.findall(r'(?:import|from)\s+([\w.]+)', content),
            "has_main": '__main__' in content,
            "todo_count": content.lower().count('todo'),
            "fixme_count": content.lower().count('fixme')
        }
        return analysis
    
    def find_issues(self, path):
        content = self.read_file(path)
        if not content:
            return []
        
        issues = []
        lines = content.split('\n')
        for i, line in enumerate(lines, 1):
            if 'FIXME' in line or 'TODO' in line:
                issues.append({"line": i, "issue": line.strip(), "type": "todo"})
            if 'except:' in line and 'pass' in line:
                issues.append({"line": i, "issue": "Bare except:pass", "type": "bug"})
            if 'print(' in line and 'def ' not in line:
                issues.append({"line": i, "issue": "Debug print", "type": "cleanup"})
        return issues
    
