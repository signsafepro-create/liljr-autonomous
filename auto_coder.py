    # ─── SELF-HEALING ───
    def fix_bare_excepts(self, path):
        """Fix bare except:pass patterns."""
        content = self.read_file(path)
        if not content:
            return {"error": "File not found"}
        
        fixed = re.sub(r'except:\s*\n\s*pass', 'except Exception as e:\n        print(f"Error: {e}")', content)
        if fixed != content:
            self.write_file(path, fixed)
            return {"status": "fixed", "path": path, "changes": "bare_excepts"}
        return {"status": "no_changes"}
    
    def add_error_handling(self, path):
        """Wrap functions with try/except."""
        content = self.read_file(path)
        if not content:
            return {"error": "File not found"}
        
        # Find functions without try blocks and wrap them
        pattern = r'(def\s+\w+\([^)]*\):\n)((?:\s+[^\n]*\n)+?)(?=\n\s*def|\Z)'
        
        def wrap_match(m):
            func_def = m.group(1)
            body = m.group(2)
            if 'try:' not in body:
                indented_body = '\n'.join(['    try:' + ('' if i == 0 else '    ' + line) 
                                           for i, line in enumerate(body.split('\n'))])
                return func_def + indented_body + '\n    except Exception as e:\n        print(f"Error: {e}")\n'
            return m.group(0)
        
        fixed = re.sub(pattern, wrap_match, content, flags=re.DOTALL)
        if fixed != content:
            self.write_file(path, fixed)
            return {"status": "enhanced", "path": path}
        return {"status": "no_changes"}
    
    # ─── AUTONOMOUS ACTIONS ───
    def analyze_project(self):
        """Analyze all files and report what needs work."""
        files = self.list_files(r'.*\.py$')
        report = {
            "files": [],
            "total_lines": 0,
            "total_functions": 0,
            "total_todos": 0,
            "total_fixmes": 0,
            "recommendations": []
        }
        
        for f in files:
            analysis = self.analyze_file(f)
            issues = self.find_issues(f)
            report["files"].append({"path": f, "analysis": analysis, "issues": issues})
            report["total_lines"] += analysis.get("lines", 0)
            report["total_functions"] += len(analysis.get("functions", []))
            report["total_todos"] += analysis.get("todo_count", 0)
            report["total_fixmes"] += analysis.get("fixme_count", 0)
        
        if report["total_todos"] > 0:
            report["recommendations"].append(f"Address {report['total_todos']} TODOs")
        if report["total_fixmes"] > 0:
            report["recommendations"].append(f"Fix {report['total_fixmes']} FIXMEs")
        if len(files) < 5:
            report["recommendations"].append("Project is small. Consider adding more modules.")
        
        return report
    
    def auto_fix(self):
        """Automatically fix common issues across project."""
        files = self.list_files(r'.*\.py$')
        results = []
        for f in files:
            r = self.fix_bare_excepts(f)
            if r.get("status") == "fixed":
                results.append(r)
        return {"fixed_files": results, "count": len(results)}
    
    def generate_module_for(self, purpose, functions_needed):
        """Generate a new module based on requirements."""
        name = purpose.lower().replace(' ', '_')
        funcs = []
        for func_name, desc in functions_needed:
            funcs.append((func_name, desc, [], ['pass']))
        
        code = self.generate_python_module(name, purpose, funcs)
        path = f"{name}.py"
        self.write_file(path, code)
        return {"status": "generated", "path": path, "purpose": purpose}


